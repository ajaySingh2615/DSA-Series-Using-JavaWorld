package com.functionsAndMethods;

public class ProductOfTwoNumbers {
    public static int productOfNumbers(int a, int b){
        return a * b;
    }

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        System.out.println(productOfNumbers(num1, num2));
    }
}
